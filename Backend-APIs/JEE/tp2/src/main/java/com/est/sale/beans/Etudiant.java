package com.est.sale.beans;

public class Etudiant {
    private String nom;
    private double note;

    public Etudiant() {}

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public double getNote() {
        return note;
    }

    public void setNote(double note) {
        this.note = note;
    }
}
